#include "libft.h"

size_t	ft_strlcpy(char *dst, const char *src, size_t dstsize)
{
	size_t	i;

	i = 0;
	while (dstsize - i > 1)
	{
		dst[i] = src[i];
		i++;
	}
	dst[i] = '\0';
	return (0);
}	
