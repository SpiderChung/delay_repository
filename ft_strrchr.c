#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int		i;
	char	*r;

	r = (char *)s;
	i = ft_strlen(r);
	while (i >= 0)
	{
		if (r[i] == c)
			return (r + i);
		i--;
	}
	return (0);
}
