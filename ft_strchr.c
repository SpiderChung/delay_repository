#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	int		i;
	char	*r;

	i = 0;
	r = (char *)s;
	while (r[i] != c)
	{
		if (r[i] == '\0')
			return (NULL);
		i++;
	}
	return (r + i);
}
